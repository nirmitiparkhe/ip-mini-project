<h2>HTML Iframes</h2>
<p>
An iframe in HTML allows you to embed another HTML document within your current
webpage. It’s like having a mini window that displays content from an external source. It
supports various attributes for specifying dimensions, borders, scrolling, and more to customize
the appearance and behaviour of the embedded content.
he src attribute specifies the URL of the document you want to embed.
Iframes can include videos, maps, or entire web pages from other sources.
</p>
<h2>HTML Layout</h2>
<p>
HTML layout refers to the structure of a web page, achieved through elements like header,
nav, main, article, section, aside, and footer. These elements help organize content, define the
page’s sections, and enhance accessibility and SEO.<br>
Header: The part of the front end which is used at the top of the page. header tag is
used to add a header section on web pages.<br>
Navigation bar: The navigation bar is the same as the menu list. It is used to display the
content information using hyperlinks. <nav> tag is used to add the nav section(nav elements) in
web pages.<br>
Index / Sidebar: It holds additional information or advertisements and is not always
necessary to be added to the page.<br>

Content Section: The content section is the central part where content is displayed. main
tag is used to add the main content of the webpages.<br>
Footer: The footer section contains the contact information and other query related to
web pages. The footer section is always put on the bottom of the web pages. The footer tag
sets the footer on web pages.<br>

