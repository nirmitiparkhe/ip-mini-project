<h2>HTML Paragraphs</h2>
<p>The p tag in HTML defines a paragraph. These have both opening and closing tags. So
anything mentioned within p and p is treated as a paragraph. A paragraph is a block-level
element so a new paragraph always begins on a new line, and browsers naturally put some
space before and after a paragraph to make it look neat and easy to read.
Properties of the paragraph tag:
As already mentioned, the p tag automatically adds space before and after any paragraph,
which is basically margins added by the browser.
If a user adds multiple spaces, the browser reduces them to a single space.
If a user adds multiple lines, the browser reduces them to a single line.
By default, the display of the Paragraph element is set to “block” which you can change using
CSS. This means that if you add another paragraph to the webpage the next paragraph will be
inserted in the next line on the webpage.
</p>
